import path from 'node:path';
import { cp, readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { discoverProject, formatSuggestedConfig } from '../discover/discover-project.mjs';
import { ensureDir, fileExists, removeDir, toPosixPath } from '../util/fs.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const PACKAGE_ROOT = path.resolve(__dirname, '..', '..', '..');
const TEMPLATE_ROOT = path.join(PACKAGE_ROOT, 'templates', 'consumer');
const CLAUDE_MARKERS = {
  begin: '<!-- ui-evidence:claude:begin -->',
  end: '<!-- ui-evidence:claude:end -->',
};
const AGENTS_MARKERS = {
  begin: '<!-- ui-evidence:agents:begin -->',
  end: '<!-- ui-evidence:agents:end -->',
};
const GENERATED_HEADER = '<!-- generated by ui-evidence install -->';

function normalizeAgent(agent) {
  if (!agent || agent === true) {
    return 'both';
  }
  if (!['claude', 'codex', 'both'].includes(agent)) {
    throw new Error(`Unknown agent target "${agent}". Use claude, codex, or both.`);
  }
  return agent;
}

function replaceTemplateTokens(source, tokens) {
  return Object.entries(tokens).reduce(
    (result, [key, value]) => result.replaceAll(`{{${key}}}`, value),
    source,
  );
}

async function readTemplate(relativePath) {
  return readFile(path.join(TEMPLATE_ROOT, relativePath), 'utf8');
}

async function writeGeneratedFile(targetPath, content, options = {}) {
  const managedContent = `${GENERATED_HEADER}\n${content}`;
  if (await fileExists(targetPath)) {
    if (!options.force) {
      const existing = await readFile(targetPath, 'utf8');
      if (!existing.startsWith(GENERATED_HEADER)) {
        return { path: targetPath, status: 'skipped' };
      }
    }
  }

  await ensureDir(path.dirname(targetPath));
  await writeFile(targetPath, managedContent, 'utf8');
  return { path: targetPath, status: 'written' };
}

function buildManagedSection(title, body, markers) {
  return `## ${title}\n${markers.begin}\n${body.trim()}\n${markers.end}\n`;
}

async function upsertManagedSection(targetPath, title, body, markers) {
  const section = buildManagedSection(title, body, markers);
  if (!(await fileExists(targetPath))) {
    await ensureDir(path.dirname(targetPath));
    await writeFile(targetPath, `${section}`, 'utf8');
    return { path: targetPath, status: 'written' };
  }

  const existing = await readFile(targetPath, 'utf8');
  const pattern = new RegExp(`${escapeRegex(markers.begin)}[\\s\\S]*?${escapeRegex(markers.end)}\\n?`, 'm');
  const next = pattern.test(existing)
    ? existing.replace(pattern, `${markers.begin}\n${body.trim()}\n${markers.end}\n`)
    : `${existing.trimEnd()}\n\n${section}`;
  await writeFile(targetPath, `${next.replace(/\s+$/, '')}\n`, 'utf8');
  return { path: targetPath, status: 'written' };
}

function escapeRegex(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

async function copyDirectory(sourceDir, targetDir, options = {}) {
  if (path.resolve(sourceDir) === path.resolve(targetDir)) {
    return { path: targetDir, status: 'skipped' };
  }

  if (await fileExists(targetDir)) {
    if (!options.force) {
      return { path: targetDir, status: 'skipped' };
    }
    await removeDir(targetDir);
  }
  await ensureDir(path.dirname(targetDir));
  await cp(sourceDir, targetDir, { recursive: true });
  return { path: targetDir, status: 'written' };
}

function buildPackageExec(packageManager) {
  if (packageManager === 'pnpm') {
    return 'pnpm exec ui-evidence';
  }
  if (packageManager === 'yarn') {
    return 'yarn ui-evidence';
  }
  if (packageManager === 'bun') {
    return 'bunx ui-evidence';
  }
  return 'npx ui-evidence';
}

function buildClaudeSection(tokens) {
  return `Use ui-evidence for before/after UI review in this repo.

- If setup looks incomplete, read \`${tokens.LOCAL_INSTALL_DOC_PATH}\` and finish the setup.
- For explicit runs, prefer the project command in \`.claude/commands/ui-evidence.md\`.
- For natural-language requests about UI comparison or screenshots, use \`${tokens.UI_EVIDENCE_EXEC}\` instead of ad hoc browser steps.
- Persist config in \`${tokens.CONFIG_PATH}\` and hooks in \`ui-evidence/hooks/*\`.
- Return the final \`review/index.html\`, \`report\`, \`manifest\`, and key comparison image paths.`;
}

function buildAgentsSection(tokens) {
  return `Use ui-evidence for UI before/after review in this repo.

- If setup is missing, read \`${tokens.LOCAL_INSTALL_DOC_PATH}\` and complete the installation.
- Prefer the local skill bundle in \`.agents/skills/ui-evidence/\` when the user asks for UI comparison, before/after screenshots, or review artifacts.
- Use \`${tokens.UI_EVIDENCE_EXEC}\` as the deterministic engine.
- Persist config in \`${tokens.CONFIG_PATH}\` and only create hooks when deterministic state requires them.
- Default human-facing output is \`review/index.html\`.`;
}

export async function scaffoldConsumerRepo(options = {}) {
  const cwd = path.resolve(options.cwd ?? process.cwd());
  const agent = normalizeAgent(options.agent);
  const configPath = path.resolve(cwd, options.config ?? 'ui-evidence.config.yaml');
  const installDocPath = path.resolve(cwd, options.installationDoc ?? path.join('docs', 'ui-evidence-installation.md'));
  const discovery = await discoverProject({ cwd });
  const tokens = {
    UI_EVIDENCE_EXEC: buildPackageExec(discovery.packageManager),
    CONFIG_PATH: toPosixPath(path.relative(cwd, configPath)),
    LOCAL_INSTALL_DOC_PATH: toPosixPath(path.relative(cwd, installDocPath)),
  };

  const actions = [];

  if (!(await fileExists(configPath)) || options.force) {
    await ensureDir(path.dirname(configPath));
    await writeFile(configPath, formatSuggestedConfig(discovery.suggestedConfig, 'yaml'), 'utf8');
    actions.push({ path: configPath, status: 'written' });
  } else {
    actions.push({ path: configPath, status: 'skipped' });
  }

  const installationTemplate = replaceTemplateTokens(
    await readTemplate(path.join('docs', 'ui-evidence-installation.md')),
    tokens,
  );
  actions.push(await writeGeneratedFile(installDocPath, installationTemplate, { force: options.force }));

  if (agent === 'claude' || agent === 'both') {
    const claudeCommandTemplate = replaceTemplateTokens(
      await readTemplate(path.join('.claude', 'commands', 'ui-evidence.md')),
      tokens,
    );
    actions.push(
      await writeGeneratedFile(
        path.resolve(cwd, '.claude', 'commands', 'ui-evidence.md'),
        claudeCommandTemplate,
        { force: options.force },
      ),
    );
    actions.push(
      await upsertManagedSection(
        path.resolve(cwd, 'CLAUDE.md'),
        'ui-evidence',
        buildClaudeSection(tokens),
        CLAUDE_MARKERS,
      ),
    );
    actions.push(
      await copyDirectory(
        path.join(PACKAGE_ROOT, 'skills', 'ui-evidence'),
        path.resolve(cwd, '.claude', 'skills', 'ui-evidence'),
        { force: options.force },
      ),
    );
  }

  if (agent === 'codex' || agent === 'both') {
    actions.push(
      await upsertManagedSection(
        path.resolve(cwd, 'AGENTS.md'),
        'ui-evidence',
        buildAgentsSection(tokens),
        AGENTS_MARKERS,
      ),
    );
    actions.push(
      await copyDirectory(
        path.join(PACKAGE_ROOT, 'skills', 'ui-evidence'),
        path.resolve(cwd, '.agents', 'skills', 'ui-evidence'),
        { force: options.force },
      ),
    );
  }

  return {
    discovery,
    tokens,
    actions,
  };
}
